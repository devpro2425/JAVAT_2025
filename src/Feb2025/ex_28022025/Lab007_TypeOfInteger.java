package Feb2025.ex_28022025;

public class Lab007_TypeOfInteger {
    public static void main(String[] args) {
        //as a integer can add a any word expect keyword
        int top=10;
        System.out.println(top);
        //as a integer can add a alphabet and under score
        int a_=123;
        System.out.println(a_);
        //as a integer can add $ sign
        int $=120;
        System.out.println($);
        //as a integer can add $and values
        int $25=-50;
        System.out.println($25);
        //example of invalid integer
        // int 123
        /*
        int 123
        int @abcd
        int #456
        * */
    }
}
